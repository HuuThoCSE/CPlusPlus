#include<iostream>
using namespace std;
main(){
	float a, b;
	cout << "Nhap vao so thuc a: ";
	cin >> a;
	cout << "Nhap vao so thuc b: ";
	cin >> b;
	cout << "\n---------------------------------\n";
	cout << "Tong cua 2 so thuc a, b: " << a + b << endl;
	cout << "Tich cua 2 so thuc a, b: " << a * b << endl;
	cout << "Hieu cua 2 so thuc a, b: " << a - b << endl;
	cout << "Thuong cua 2 so thuc a, b: " << a / b << endl;
}
